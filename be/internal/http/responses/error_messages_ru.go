func localizeErrorMessage(msg string) string {
	normalized := strings.TrimSpace(msg)
	if normalized == "" {
		return msg
	}
	if localized, ok := errorMessageRuMap[normalized]; ok {
		return localized
	}
	if strings.HasPrefix(normalized, "Child #") && strings.Contains(normalized, "is not an existing t_uuid") {
		return strings.Replace(
			strings.Replace(normalized, "Child #", "Элемент #", 1),
			"is not an existing t_uuid",
			"не является существующим t_uuid.",
			1,
		)
	}
	return msg
}
