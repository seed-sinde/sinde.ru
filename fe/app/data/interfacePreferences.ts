export const interfaceLocaleToTag = (code: InterfaceLocaleCode): string => {
  switch (code) {
    case 'en':
      return 'en-US'
    case 'ch':
      return 'zh-CN'
    case 'jp':
      return 'ja-JP'
    default:
      return 'ru-RU'
  }
}
export const normalizeInterfaceLocale = (raw: unknown): InterfaceLocaleCode => {
  const normalized = String(raw || '')
    .trim()
    .toLowerCase()
  if (!normalized) return 'ru'
  if (normalized === 'ru' || normalized.startsWith('ru-')) return 'ru'
  if (normalized === 'en' || normalized.startsWith('en-')) return 'en'
  if (normalized === 'ch' || normalized === 'zh' || normalized.startsWith('zh-')) return 'ch'
  if (normalized === 'jp' || normalized === 'ja' || normalized.startsWith('ja-')) return 'jp'
  return 'ru'
}
export const normalizeThemePreference = (raw: unknown): ThemePreference => {
  const normalized = String(raw || '')
    .trim()
    .toLowerCase()
  if (normalized === 'light') return 'light'
  if (normalized === 'dark') return 'dark'
  return 'system'
}
